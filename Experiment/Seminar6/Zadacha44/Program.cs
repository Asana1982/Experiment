// Не используя рекурсию, выведите первые N чисел Фибоначчи.
// Первые два числа Фибоначчи: 0 и 1.

Console.Clear();
Console.Write("Введите число: ");
int n = Convert.ToInt32(Console.ReadLine()), a0 = 0, a1 = 1, x; //начальные значения
for (int i = 0; i < n; i++)
{
  Console.Write($"{a0} "); // выводим первое значение
  x = a0 + a1;
  a0 = a1; // перестановка значений
  a1 = x;
}